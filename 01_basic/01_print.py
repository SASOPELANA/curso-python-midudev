# El modulo print() se utiliza para mostrar información en la consola.
# Es una función incorporada en Python que permite imprimir texto,
# números u otros tipos de datos.
print("¡Hola Mundo!")
print("Esto tambein funciona con comillas simples.")

print(" ")

print("Python", "Español", "Genial")

# Si queremos cambiar el separador entre los elementos,
# podemos usar el argumento 'sep' en la función print().
# Por defecto, el separador es un espacio,
# pero podemos cambiarlo a cualquier otro carácter o cadena de caracteres.
print("Separador", "con", "guion", sep="-")

# El argumento 'end' se utiliza para especificar lo que se imprime al
# final de la salida.
print("Esto se imprime", end=" ")
print("en la misma línea.")
