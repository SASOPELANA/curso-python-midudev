print("\n ------- Booleanos en Python ------- \n")
print("Valores booleanos básicos.")
print("True:", True)
print("False:", False)

# Operadores de comparación

print("5 > 4:", 5 > 4)  # True
print("5 < 4:", 5 < 4)  # False
print("5 == 5:", 5 == 5)  # True
print("5 != 4:", 5 != 4)  # True
print("5 >= 5:", 5 >= 5)  # True
print("5 <= 4:", 5 <= 4)  # False

# Comparación de cadenas
print("\nConparación de cadenas")
print("Manzana < Pera", "Manzana" < "Pera")  # True (orden alfabético)
print("'Hola' == 'hola'", "Hola" == "hola")  # False (diferencia de mayúsculas)

# Operadores lógicos
print("\nOperadores lógicos\n")
print("True and False:", True and False)  # False
print("True or False:", True or False)  # True
print("not True:", not True)  # False
print("not False:", not False)  # True
print("True and True:", True and True)  # True
print("False or False:", False or False)  # False
